
/**
 * add - add 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - calculate the modules of the division between 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
